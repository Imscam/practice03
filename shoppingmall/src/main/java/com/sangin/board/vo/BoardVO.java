package com.sangin.board.vo;

public class BoardVO {
	
	private Long clothNum;
	private String clothCode;
	private String clothName;
	private Long clothPrice;
	private Long clothInventory;
	private String clothImage;
	private String clothImageName;
	private String clothInfo;
	private String clothSize;
	public Long getClothNum() {
		return clothNum;
	}
	public void setClothNum(Long clothNum) {
		this.clothNum = clothNum;
	}
	public String getClothCode() {
		return clothCode;
	}
	public void setClothCode(String clothCode) {
		this.clothCode = clothCode;
	}
	public String getClothName() {
		return clothName;
	}
	public void setClothName(String clothName) {
		this.clothName = clothName;
	}
	public Long getClothPrice() {
		return clothPrice;
	}
	public void setClothPrice(Long clothPrice) {
		this.clothPrice = clothPrice;
	}
	public Long getClothInventory() {
		return clothInventory;
	}
	public void setClothInventory(Long clothInventory) {
		this.clothInventory = clothInventory;
	}
	public String getClothImage() {
		return clothImage;
	}
	public void setClothImage(String clothImage) {
		this.clothImage = clothImage;
	}
	public String getClothImageName() {
		return clothImageName;
	}
	public void setClothImageName(String clothImageName) {
		this.clothImageName = clothImageName;
	}
	public String getClothInfo() {
		return clothInfo;
	}
	public void setClothInfo(String clothInfo) {
		this.clothInfo = clothInfo;
	}
	public String getClothSize() {
		return clothSize;
	}
	public void setClothSize(String clothSize) {
		this.clothSize = clothSize;
	}
	
}
	
